function normalizeLocale(raw: unknown): Locale | null {
  const s = String(raw || "").trim().toLowerCase()
  if (!s) return null
  const base = s.split("-")[0] as Locale
  return (SUPPORTED_LOCALES as readonly string[]).includes(base) ? (base as Locale) : null
}

function detectLocale(): Locale {
  try {
    if (typeof window !== "undefined") {
      const stored = normalizeLocale(window.localStorage.getItem(LOCALE_STORAGE_KEY))
      if (stored) return stored

      const langs = (navigator.languages && navigator.languages.length ? navigator.languages : [navigator.language]).filter(Boolean)
      for (const l of langs) {
        const n = normalizeLocale(l)
        if (n) return n
      }
    }
  } catch {
    // ignore
  }
  return "en"
}

type I18nContextValue = {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: string) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function flagForLocale(locale: Locale): string {
  return FLAGS[locale] || FLAGS.en
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en")

  useEffect(() => {
    setLocaleState(detectLocale())
  }, [])

  useEffect(() => {
    try {
      if (typeof document !== "undefined") {
        document.documentElement.lang = locale
      }
    } catch {
      // ignore
    }
  }, [locale])

  const setLocale = useCallback((next: Locale) => {
    setLocaleState(next)
    try {
      if (typeof window !== "undefined") {
        window.localStorage.setItem(LOCALE_STORAGE_KEY, next)
      }
    } catch {
      // ignore
    }
  }, [])

  const t = useCallback(
    (key: string) => {
      return DICTS[locale]?.[key] ?? DICTS.en[key] ?? key
    },
    [locale],
  )

  const value = useMemo<I18nContextValue>(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error("useI18n must be used within I18nProvider")
  return ctx
}

